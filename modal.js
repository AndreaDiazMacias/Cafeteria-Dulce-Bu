// Capurar el modal
var modal = document.getElementById("myModal");

// Capurar el boton que abre el modal
var btn = document.getElementById("myBtn");

//Capurar el <span> que cierra el modal
var span = document.getElementsByClassName("cerrar")[0];

// When the user clicks on the button, Abrir modal
btn.onclick = function() {
  modal.style.display = "block";
}

//Cuando el usuario hace click sobre el <span>, cierra el modal
span.onclick = function() {
  modal.style.display = "none";
}

//Cuando el usuario hace click en cualquier lugar fuera del modal, se cierra
window.onclick = function(event) {
  if (event.target == modal) {
    modal.style.display = "none";
  }
}

